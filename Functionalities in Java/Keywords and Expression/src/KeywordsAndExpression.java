public class KeywordsAndExpression {
    public static void main(String[] args) {


//  Keywords :
//  Java keywords are also known as reserved words. Keywords are particular words that act as a key to a code.
//  These are predefined words by Java, so they cannot be used as a variable or object name or class name.
//  There are 48 keywords in Java.

        int int1 = 10; // int is a keyword you can't use as a variable (int)
        System.out.println(int1);

//  Expression :
//  An expression is a series of variables, operators, and method calls
//  (constructed according to the syntax of the language) that evaluates to a single value.

        String myName = "Pankaj Kumar Sah"; // Only "Pankaj Kumar Sah" is the expression
        System.out.println("My name is " + myName);



    }
}
